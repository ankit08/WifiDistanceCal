package com.aricent.wifidistancecal;

public class CircleCoordinates {

	private double circleInterX;
	private double circleInterY;
	private double circleDistX;
	private double circleDistY;
	
	
	
	public double getCircleInterX() {
		return circleInterX;
	}
	public void setCircleInterX(double circleInterX) {
		this.circleInterX = circleInterX;
	}
	public double getCircleInterY() {
		return circleInterY;
	}
	public void setCircleInterY(double circleInterY) {
		this.circleInterY = circleInterY;
	}
	public double getCircleDistX() {
		return circleDistX;
	}
	public void setCircleDistX(double circleDistX) {
		this.circleDistX = circleDistX;
	}
	public double getCircleDistY() {
		return circleDistY;
	}
	public void setCircleDistY(double circleDistY) {
		this.circleDistY = circleDistY;
	}

}
